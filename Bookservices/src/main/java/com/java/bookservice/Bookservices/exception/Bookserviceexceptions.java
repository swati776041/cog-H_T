package com.java.bookservice.Bookservices.exception;

public class Bookserviceexceptions extends Exception{

	public Bookserviceexceptions() {
		super();
		
	}

	public Bookserviceexceptions(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public Bookserviceexceptions(String message, Throwable cause) {
		super(message, cause);
		
	}

	public Bookserviceexceptions(String message) {
		super(message);
		
	}

	public Bookserviceexceptions(Throwable cause) {
		super(cause);
		
	}

}
